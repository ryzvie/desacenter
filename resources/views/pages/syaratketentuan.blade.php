Selamat Datang di Program Desabrilian 2021

Syarat dan Ketentuan yang ditetapkan di bawah ini mengatur terkait penggunaan situs www.desacenter.id  (situs). Pengguna dan peserta program disarankan membaca dengan seksama karena dapat berdampak kepada hak dan kewajiban pengguna di bawah hukum.

Pengguna dan peserta program harus merupakan pengurus maupun yang menjabat dalam keanggotan desa serta aktif dalam pengelolaan desa. Segala tindakan yang dapat membatalkan anda dalam keikutsertaan program merupakan hak dan tanggung jawab dari pelaksana program dan melalui pertimbangan secara menyeluruh.

Dengan mendaftar dan/ atau menggunakan situs www.desacenter.id, maka pengguna dianggap telah membaca, mengerti, memahami dan menyetujui semua isii dalam Syarat dan Ketentuan Program DesaBrilian2021. Syarat dan ketentuan ini merupakan kesepakatan yang dituangkan dalam sebuah perjanjian yang sah antara pelaksana program, penyedia platform desacenter.id dan pengguna (user). Jika Pengguna tidak menyetujui salah satu, sebagian, atau seluruh isi Syarat dan Ketentuan, maka Pengguna dapat menghentikan penggunaan layanan di Situs. Pengguna mendaftar program berarti telah mengerti tentang segala hal yang terkandung dalam  syarat dan ketentuan yang sudah tertuang dalam situs ini. 

Definisi
Program Desa Brilian adalah desa binaan BRI yang memiliki seluruh atau kombinasi dari empat aspek utama. Mulai dari penggerak ekonomi desa, digitalisasi desa, desa kreatif, dan desa yang dapat meningkatkan kesejahteraan masyarakat desa.
Tujuan Program Desa brilian adalah
Meningkatkan kualitas pengelilaan desa yang meninspirasi untuk memajuka desa dan BUMdes
Bentuk Penghargaan yang akan menampilkan desa yang berkepedulian sosial, menggerakan perubahan dan berperan inovatif memotivasi orang di sekitarnya untuk maju
Menyebarluaskan informasi pemberdayaan masyarakat yang sudah dilakukan oleh BRI (influencer BRI)
Memperkuat posisi BRI sebagai Bank yang peduli kepada desa dan masyarakat desa
Sebagai salah satu tools dalam pembinaan dan mendekatkan diri (engagement) kepada seluruh komponen masyarakat di Desa.


    
Pendaftaran Akun dan Pendaftaran Peserta Program Desabrilian 2021
Pendaftaran akun adalah melalui platform www.brilian.desacenter.id 
Akun menggunakan akun dari pengurus desa yang mengikuti program desabrilian
Gabung/Join Desa sesuai desa anda
Nomor handphone yang tertera merupakan nomor handphone yang aktif dan memiliki aplikasi whatsapp.
Pendaftar akun juga harus mempunyai email yang masih aktif.
Tidak diperkenan untuk mewakilkan pendaftaran akun kepada orang lain.
Pendaftaran peserta program merupakan peserta yang sudah tergabung dalam desa.
Member program adalah peserta yang sudah melakukan registrasi dan sudah tergabung dalam desa dan didaftarkan oleh salah satu penanggung jawab atau perwakilan desa.
Member program adalah 4 orang dan dianjurkan terdiri dari : Kepala Desa atau yang mewakili, Ketua BPD atau tokoh mogram inkubasi desa Brilian, Direktur Bumdes atau yang mewakili, Pengurus Desa dan atau pengurus bumdes yang mewakili.
Penetapan dan seleksi program desabrilian 2021
Seluruh proses penetapan dan seleksi desa terpilih adalah dari panitia program desabrilian 2021
Desa yang terpilih dan lolos seleksi wajib mengikuti syarat dan ketentuan yang berlaku dalam program Desa Brilian 2021
Pemilihan dan penetapan seleksi telah melalui proses yang ketat dan melalui berbagai pertimbangan dan kebijakan panitia Desa Brilian 2021
Apabila dalam proses seleksi dan penetapan terdapat peserta yang mengundurkan diri harus melaporkan terlebih dahulu kepada panitia Desa Brilian 2021 untuk dapat dilakukan proses lebih lanjut
Penentuan Nominasi dan pemenang desabrilian 2021
Nominasi adalah kadindat dari seluruh peserta yang terseleksi dan masuk dalam kategori Nominasi yang telah disusun oleh Panitia Desa Brilian.
Pemenang adalah peserta yang lolos dalam kategori penilaian pemenang yang sudah disusun oleh panitia Desa Brilian 2021.
Pengumuman nominasi dan pemenang akan dilaksanakan pada jadwal acara yang sudah disusun oleh panitia.
Panitia berhak menggugurkan status nominasi dan pemenang, apabila peserta yang masuk nominasi dan pemenang melakukan segala hal yang tidak sesuai dengan ketentuan yang berlaku dan melanggar hukum.
Peserta yang masuk Nominasi dan Pemenang wajib tunduk kepada segala hal yang telah ditetapkan oleh Panitia Desa Brilian 2021
Penilaian program desabrilian
Penilaian program desabrilian dilakukan oleh Panitia Desa Brilian 2021 dengan segala aspek yang terkandung dalam program Desa Brilian 2021
Aspek peniliaian tersebut diantaranya adalah :
Keaktifan peserta dalam mengikuti kegiatan program Desa Brilian
Kelengkapan Administrasi Manajemen dan Kelembagaan
Digitalisasi Desa
Observasi lapangan oleh Panitia Desa Brilian 2021
Para peserta wajib dan tunduk atas segala hasil penilian yang dilakukan oleh Panitia Desa Brilian 2021
Segala penilaian dapat digugurkan apabila peserta melanggar syarat dan ketentuan yang telah ditetapkan oleh panitia.
Panitia berhak dan berwenang dalam penetapan dan penilaian peserta Desa Brilian 2021
 
